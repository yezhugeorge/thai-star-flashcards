# 生成词语拆解
# ============================================================
def gen_breakdown(text):
    words = segment_thai(text)
    breakdown = []
    for w in words:
        if w in THAI_DICT:
            breakdown.append({'w': w, 'm': THAI_DICT[w][2]})
        else:
            breakdown.append({'w': w, 'm': '?'})
    return breakdown

