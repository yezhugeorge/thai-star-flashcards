    words = segment_thai(text)
    roman_parts = []
    zhuyin_parts = []
    for w in words:
        if w in THAI_DICT:
            roman_parts.append(THAI_DICT[w][0])
            zhuyin_parts.append(THAI_DICT[w][1])
        else:
            # 未知字符，跳过
            pass
    roman = ' '.join(roman_parts)
    zhuyin = ' '.join(zhuyin_parts)
    return roman, zhuyin

