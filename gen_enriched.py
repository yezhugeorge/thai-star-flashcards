VARIATIONS = {
    'น่ารัก': [
        {'t': 'น่ารักที่สุด', 'c': '最可爱了'},
        {'t': 'น่ารักเกินไป', 'c': '太可爱了吧'},
    ],
    'หล่อ': [
        {'t': 'หล่อที่สุด', 'c': '最帅了'},
        {'t': 'หล่อเกินไป', 'c': '太帅了吧'},
    ],
    'สวย': [
        {'t': 'สวยที่สุด', 'c': '最美了'},
        {'t': 'สวยมาก', 'c': '好美'},
    ],
    'รัก': [
        {'t': 'รักที่สุด', 'c': '最爱'},
        {'t': 'รักตลอดไป', 'c': '永远爱'},
    ],
    'หวาน': [
        {'t': 'หวานที่สุด', 'c': '最甜了'},
        {'t': 'หวานเกิน', 'c': '太甜了'},
    ],
    'ดี': [
        {'t': 'ดีที่สุด', 'c': '最好了'},
        {'t': 'ดีมาก', 'c': '很好'},
    ],
}

def gen_variations(text, cn):
    """根据句子内容生成变体"""
    vars = []
    for key, examples in VARIATIONS.items():
        if key in text:
            for ex in examples:
                if ex['t'] != text:
                    vars.append(ex)
    # 通用变体：加/去 "มาก"
    if 'มาก' in text and len(vars) < 2:
        without_mak = text.replace('มาก', '').strip()
        if without_mak and without_mak != text:
            vars.append({'t': without_mak, 'c': cn.replace('很', '').replace('非常', '').strip()})
    if 'มาก' not in text and len(vars) < 2:
        vars.append({'t': text + ' มาก', 'c': cn + '（很）'})
    return vars[:3]

