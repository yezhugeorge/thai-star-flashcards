def enrich_sentence(sent):
    """enrich a single sentence"""
    thai = sent['thai']
    cn = sent['cn']
    sub = sent['sub']

    roman, zhuyin = gen_phonetic(thai)
    breakdown = gen_breakdown(thai)
    occasion = OCCASION_TEMPLATES.get(sub, '追星相关场景使用')
    tips = TIPS_TEMPLATES.get(sub, '注意泰语有男女语气词区别：女性用"ค่ะ"，男性用"ครับ"')
    variations = gen_variations(thai, cn)

    return {
        **sent,
        'roman': roman,
        'zhuyin': zhuyin,
        'breakdown': breakdown,
        'occasion': occasion,
        'tips': tips,
        'variations': variations,
    }

def enrich_dialogue(dlg):
    """enrich dialogue turns with phonetic"""
    new_turns = []
    for turn in dlg['turns']:
        roman, zhuyin = gen_phonetic(turn['t'])
        new_turns.append({
            **turn,
            'roman': roman,
            'zhuyin': zhuyin,
        })
    return {**dlg, 'turns': new_turns}

