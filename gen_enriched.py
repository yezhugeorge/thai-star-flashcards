# 泰语分词 (最长匹配)
# ============================================================
def segment_thai(text):
    """用最长匹配算法分词"""
    words = []
    i = 0
    while i < len(text):
        matched = False
        # 尝试从最长到最短匹配
        for length in range(min(20, len(text) - i), 0, -1):
            chunk = text[i:i+length]
            # 跳过空格和标点
            if chunk in (' ', '\t', '\n', '.', ',', '!', '?', 'ๆ', '~', '-'):
                if chunk == ' ':
                    pass  # 跳过空格
                i += length
                matched = True
                break
            if chunk in THAI_DICT:
                words.append(chunk)
                i += length
                matched = True
                break
        if not matched:
            # 单字符作为未知词
            if text[i] not in (' ', '\t', '\n', '.', ',', '!', '?', 'ๆ', '~', '-'):
                words.append(text[i])
            i += 1
    return words

